package operators;

public class EqualityOperators {
    public static void main(String[] args) {
        int a = 10,b = 20, c = 10;
        System.out.println(a==b);//10==20=>false
        System.out.println(a==c);//10==10=>true
        System.out.println(a!=b);//10!=20=>true
        System.out.println(a!=c);//10!=10=>false
    }


}
