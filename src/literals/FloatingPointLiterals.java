package literals;

public class FloatingPointLiterals {
    public static void main(String[] args) {
        //float values :-
        float a = 12.234f;
        float b = 13.456F;
        float num1 = 13.1234567890f;

        System.out.println(num1);

        //double values :-
        double c = 12.34556d;
        double d = 344.234344D;
        double e = 34.6677;
        double num2 = 13.1234567890;
        System.out.println(num2);
    }
}
