package literals;

public class BackSlashLiteral {
    public static void main(String[] args) {
        //new line :-
        System.out.println("this is digital partners Institute.\nWe are Learning software testing.\nStarted with java module basics in automation testing.");

        //horizontal tab :-
        System.out.println("apple\tball\t\tcat\t\t\tdog\t\t\t\tegg");

        //backspace :-
        System.out.println("softwareeeeee\b\b\b\b\b testing");

        //single quotes :-
        System.out.println("cooking is my \'favourite\' hobby food is always \'delicious\'");

        //double quotes :-
        System.out.println("elon musk said that \"aliens might exist among us!!\"");
    }
}
